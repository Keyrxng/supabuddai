export type RLSPolicy = {
  schema_name: string;
  table_name: string;
  policy_name: string;
  policy_condition: string | null;
  policy_check: string | null;
  command: string;
  role: string;
};

export const columns: ColumnDef<RLSPolicy>[] = [
  {
    accessorKey: "schema_name",
    header: "Schema",
  },
  {
    accessorKey: "table_name",
    header: "Table",
  },
  {
    accessorKey: "policy_name",
    header: "Policy Name",
  },
  {
    accessorKey: "policy_condition",
    header: "Policy Condition",
  },
  {
    accessorKey: "policy_check",
    header: "Policy Check",
  },
  {
    accessorKey: "command",
    header: "Command",
  },
  {
    accessorKey: "role",
    header: "Role",
  },
  {
    id: "select",
    header: ({ table }) => (
      <Checkbox
        checked={
          table.getIsAllPageRowsSelected() ||
          (table.getIsSomePageRowsSelected() && "indeterminate")
        }
        onCheckedChange={(value) => table.toggleAllPageRowsSelected(!!value)}
        aria-label="Select all"
      />
    ),
    cell: ({ row }) => (
      <Checkbox
        checked={row.getIsSelected()}
        onCheckedChange={(value) => row.toggleSelected(!!value)}
        aria-label="Select row"
      />
    ),
    enableSorting: false,
    enableHiding: false,
  },
];

function jsonToCSV(json, project, user) {
  const rows = [];

  const headers = Object.keys(json[0]);
  rows.push(headers.join(","));

  json.forEach((obj) => {
    const row = headers
      .map((hdr) => JSON.stringify(obj[hdr], null, 2))
      .join(",");
    rows.push(row);
  });

  const joinedRows = rows.join("\n");

  return joinedRows;
}

const supabase = createClientComponentClient({
  supabaseKey: process.env.NEXT_PUBLIC_SUPABASE_KEY,
  supabaseUrl: process.env.NEXT_PUBLIC_SUPABASE_URL,
});

export function PolicyDataTable({ project }: { project: string }) {
  const [sorting, setSorting] = React.useState<SortingState>([]);
  const [rls, setRls] = React.useState<RLSPolicy[]>([]);
  const [rowSelection, setRowSelection] = React.useState({});
  const [columnVisibility, setColumnVisibility] =
    React.useState<VisibilityState>({});

  const [columnFilters, setColumnFilters] = React.useState<ColumnFiltersState>(
    []
  );
  const rlsFetching = async () => {
    const resp = await fetch("/api/rls", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        name: project,
      }),
    });

    const data = await resp.json();

    setRls(data);
    const { data: user } = await supabase.auth.getUser();
    const file = jsonToCSV(data, project, user.user.id);

    const element = document.createElement("a");
    const fileBlob = new Blob([file], { type: "text/plain" });
    element.href = URL.createObjectURL(fileBlob);
    element.download = "rls.csv";
    document.body.appendChild(element);
    element.click();
  };

  const table = useReactTable({
    data: rls,
    columns,
    onSortingChange: setSorting,
    onColumnFiltersChange: setColumnFilters,
    getCoreRowModel: getCoreRowModel(),
    getPaginationRowModel: getPaginationRowModel(),
    getSortedRowModel: getSortedRowModel(),
    getFilteredRowModel: getFilteredRowModel(),
    onColumnVisibilityChange: setColumnVisibility,
    onRowSelectionChange: setRowSelection,
    state: {
      sorting,
      columnFilters,
      columnVisibility,
      rowSelection,
    },
  });

  return (
    <div className="grid relative gap-4 h-full max-h-[350px] w-auto overflow-y-auto">
      <div className="flex items-center justify-end space-x-2 py-4">
        <div className="flex-1 text-sm text-muted-foreground">
          {table.getFilteredSelectedRowModel().rows.length} of{" "}
          {table.getFilteredRowModel().rows.length} row(s) selected.
        </div>

        <Button
          variant="outline"
          onClick={() => rlsFetching()}
          className="right-0 "
        >
          Aggregrate
        </Button>
        <div className="space-x-2">
          <Button
            variant="outline"
            size="sm"
            onClick={() => table.previousPage()}
            disabled={!table.getCanPreviousPage()}
          >
            Previous
          </Button>
          <Button
            variant="outline"
            size="sm"
            onClick={() => table.nextPage()}
            disabled={!table.getCanNextPage()}
          >
            Next
          </Button>
        </div>
      </div>
      <div>
        <div className="rounded-md border">
          <Table>
            <TableHeader>
              {table.getHeaderGroups().map((headerGroup) => (
                <TableRow key={headerGroup.id}>
                  {headerGroup.headers.map((header) => (
                    <TableHead key={header.id}>
                      {flexRender(
                        header.column.columnDef.header,
                        header.getContext()
                      )}
                    </TableHead>
                  ))}
                </TableRow>
              ))}
            </TableHeader>
            <TableBody>
              {table.getRowModel().rows.map((row) => (
                <TableRow key={row.id}>
                  {row.getVisibleCells().map((cell) => (
                    <TableCell key={cell.id}>
                      {flexRender(
                        cell.column.columnDef.cell,
                        cell.getContext()
                      )}
                    </TableCell>
                  ))}
                </TableRow>
              ))}
            </TableBody>
          </Table>
        </div>
      </div>
    </div>
  );
}
